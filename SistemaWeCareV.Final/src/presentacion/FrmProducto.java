    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        // TODO add your handling code here:
        if (jcb_tipo.getSelectedIndex()==0 || jtf_nombre_producto.getText().equals("") || jtf_precio.getText().equals("") || jtf_marca.getText().equals("") || jtf_descripcion.getText().equals("") || jtf_ingreso.getText().equals("") || jtf_vencimiento.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben ser rellenados!", 
                    "ERROR", JOptionPane.WARNING_MESSAGE);
        }
        else {
            //construimos nuestro objeto con los valores del formulario
            product.setProductoID(Integer.parseInt(jtf_id.getText()));
            tprod = (Tipoproducto)this.jcb_tipo.getItemAt(this.jcb_tipo.getSelectedIndex());
            product.setTipoproductoID(tprod.getTipoproductoID());
            product.setNombreProducto(jtf_nombre_producto.getText());
            product.setPrecio(Float.parseFloat(jtf_precio.getText()));
            product.setMarca(jtf_marca.getText());
            product.setDescripcion(jtf_descripcion.getText());
            product.setFechaIngreso(jtf_ingreso.getText());
            product.setFechaVencimiento(jtf_vencimiento.getText());
            product.setEstado(2);
            
            
            //validamos que el metodo guardar devuelve un true
            if(dtp.editarProd(product)){
                JOptionPane.showMessageDialog (this, "El registro fue actualizado con éxito!", 
                  "MENSAJE", JOptionPane.INFORMATION_MESSAGE);
                actualizarTabla();
                limpiarCampos();
                    
            }
            else {
                JOptionPane.showMessageDialog(this, 
                  "Revise los datos e intente nuevamente. Si el error persiste contacte al Administrador del Sistema.", 
                  "ERROR", JOptionPane.ERROR_MESSAGE); 
            }
        }
    }//GEN-LAST:event_btn_editarActionPerformed

