    private void jtbl_productoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbl_productoMouseClicked
        // TODO add your handling code here:
        // TODO add your handling code here:
        
        //obtenemos la fila seleccionada del jtable
        int fila = this.jtbl_producto.getSelectedRow();
        int idTipo = 0; //guarda el idLocations que se obtiene de la tabla
        int size = 0; //guarda la cantidad de elementos que hay en el jcombobx de Locations
        int pos = 1; //variable de control para recorrer las posiciones de los elementos en el jcombobox
        
        //asignamos los valores a los campos del formulario
        this.jtf_id.setText(this.jtbl_producto.getValueAt(fila, 0).toString());
        this.jtf_nombre_producto.setText(this.jtbl_producto.getValueAt(fila, 2).toString());
        this.jtf_precio.setText(this.jtbl_producto.getValueAt(fila, 3).toString());
        this.jtf_marca.setText(this.jtbl_producto.getValueAt(fila, 4).toString());
        this.jtf_descripcion.setText(this.jtbl_producto.getValueAt(fila, 5).toString());
        this.jtf_ingreso.setText(this.jtbl_producto.getValueAt(fila, 6).toString());
        this.jtf_vencimiento.setText(this.jtbl_producto.getValueAt(fila, 7).toString());
        this.jtf_estado.setText(this.jtbl_producto.getValueAt(fila, 8).toString());
        
        //obtenemos el idLocations del jtable
        idTipo = Integer.parseInt(this.jtbl_producto.getValueAt(fila, 1).toString());
        size = this.jcb_tipo.getItemCount(); //obtenemos la cantidad de elementos contenidos en el jcombobox
        do{
            tprod = (Tipoproducto)this.jcb_tipo.getItemAt(pos);
            if(tprod.getTipoproductoID() == idTipo){
                //selecciono el objeto del jcombobox
                this.jcb_tipo.setSelectedIndex(pos);
                break;
            }
            pos++;
        }
        while(pos<=size);
    }//GEN-LAST:event_jtbl_productoMouseClicked

