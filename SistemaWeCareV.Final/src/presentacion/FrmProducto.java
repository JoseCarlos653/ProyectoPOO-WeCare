    private void llenarComboTipoproducto(){
        //llenamos la lista
        listTprod = dttp.listarTipoproducto();
        
        for(Tipoproducto typeproduct: listTprod){
            //llenar el combobox con los objetos de la lista
            this.jcb_tipo.addItem(typeproduct);
            //this.jcb_locations.addItem(l.toString());
        }
    }
    
    
    private void actualizarTabla(){
        myData.setColumnCount(0);
        myData.setRowCount(0);
        this.llenarTablaProducto();
    }
    
    
    private void llenarTablaProducto()
    {
         //llenamos la lista
        listProduct = dtp.listarProd();
        
        //creamos un arraylist con las columnas del modelo
        ArrayList<Object> listNombreColumnas = new ArrayList<Object>();
        listNombreColumnas.add("productoID");
        listNombreColumnas.add("tipoproductoID");
        listNombreColumnas.add("nombreProducto");
        listNombreColumnas.add("precio");
        listNombreColumnas.add("marca");
        listNombreColumnas.add("descripcion");
        listNombreColumnas.add("fechaIngreso");
        listNombreColumnas.add("fechaVencimiento");
        listNombreColumnas.add("estado");
        //recorremos cada elemento del arraylist y creamos un objeto
        for(Object column : listNombreColumnas){
            //asignamos las columnas al modelo
            myData.addColumn(column);
        }
        
       //recorremos cada elemento de la lista y creamos el objeto
        for(Producto product: listProduct){
            Object[] datosProducto = new Object[]{product.getProductoID(), product.getTipoproductoID(), product.getNombreProducto(), product.getPrecio(), product.getMarca(), product.getDescripcion(), product.getFechaIngreso(), product.getFechaVencimiento(), product.getEstado()};
            //asignamos un arreglo de objetos a una fila del modelo
            myData.addRow(datosProducto);
        }
        //Asignamos el modelo y/o coleccion de datos a la tabla
        jtbl_producto.setModel(myData);
    }
    
    
    private void filtrarTabla(){
       trsfiltro.setRowFilter(RowFilter.regexFilter(jtf_buscar.getText(),2));
    }
    
    
