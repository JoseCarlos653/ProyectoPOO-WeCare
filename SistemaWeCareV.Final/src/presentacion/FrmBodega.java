    private void bguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bguardarActionPerformed
        // TODO add your handling code here:
        //validamos que todos los campos sean ingresados por el usuario      
        if(cbWeCare.getSelectedIndex()==0||jtf_descripcion.getText().equals("")|| jtf_nombre.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Todos los campos son requeridos!", 
                    "ERROR", JOptionPane.WARNING_MESSAGE);
        }
        else{
            //construimos nuestro objeto con los valores del formulario
            w = (WeCare)this.cbWeCare.getItemAt(this.cbWeCare.getSelectedIndex());
            b.setWecareID(w.getWecareID());
            b.setDescripcion(jtf_descripcion.getText());
            b.setEstado(1);
            b.setNombreBodega(jtf_nombre.getText());       
                                                                     
           
            //validamos que el id no exista en la tabla de la bd
            if(dtb.existeBodega (b)){//*************************************************************************************
                JOptionPane.showMessageDialog(this, "la bodega que intenta ingresar ya existe!", 
                    "ERROR", JOptionPane.WARNING_MESSAGE);
                jtf_nombre.setText("");
                jtf_nombre.grabFocus();
                
            }
            else{
                //validamos que el metodo guardar devuelve un true
                if(dtb.guardarBodega(b)){//****************************************************************
                    JOptionPane.showMessageDialog (this, "El registro fue almacenado con √©xito!", 
                      "MENSAJE", JOptionPane.INFORMATION_MESSAGE);
                    actualizarTabla();
                    limpiarCampos();
                }
                else{
                   JOptionPane.showMessageDialog(this, 
                      "Revise los datos e intente nuevamente. Si el error persiste contacte al Administrador del Sistema.", 
                      "ERROR", JOptionPane.ERROR_MESSAGE); 
                }
            }
        }       
    }//GEN-LAST:event_bguardarActionPerformed

